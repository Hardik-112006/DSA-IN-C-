// Byte addressable memory is a memory organization in which each byte (8 bits) has a unique address.

// Example:
// If memory contains:

// Address	Data
// 1000	A
// 1001	B
// 1002	C

// Here, each byte (A, B, C) can be accessed separately using its own address (1000, 1001, 1002).

// One-line answer for notes:
// In byte-addressable memory, every 8-bit byte is assigned a unique address for direct access.